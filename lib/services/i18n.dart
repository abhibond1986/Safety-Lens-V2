// Lightweight i18n for SAIL Safety Lens — supports EN, HI, BN, OR
// Usage:
//   String x = I18n.t('home.welcome');
//   I18n.setLocale('hi');
//   listen to I18n.instance to rebuild on language change

import 'package:flutter/material.dart';
